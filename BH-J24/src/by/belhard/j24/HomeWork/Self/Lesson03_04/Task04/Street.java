package by.belhard.j24.HomeWork.Self.Lesson03_04.Task04;

public class Street {
    String street;
    int number;

    public Street(String street, int number) {
        this.street = street;
        this.number = number;
    }


    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
